import turtle 
lala=turtle.Pen()
lala.shape('turtle')
lala.color('red')
lala.forward(200)
lala.left(90)
lala.forward(200)
lala.left(90) 
lala.forward(200)
lala.left(90)
lala.forward(200)
lala.left(180)
lala.forward(200)
lala.right(135)
lala.forward(140)
lala.left(90)
lala.forward(140) 
lala.left(180)
lala.forward(135) 
lala.color('blue') 
lala.write('生日快乐')
lala.hideturtle()
